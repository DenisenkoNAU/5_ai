% Створити у середовищі MATLAB програму інтерполяції функції за заданими значеннями за зразком
t=1:4;
Y=[7, 5, 1, 12];
M=[t(1)^3, t(1)^2, t(1), 1;
   t(2)^3, t(2)^2, t(2), 1;
   t(3)^3, t(3)^2, t(3), 1;
   t(4)^3, t(4)^2, t(4), 1];
X=M^(-1)*Y';
t2=1:0.01:4;
P=X(1)*t2.^3+X(2)*t2.^2+X(3)*t2+X(4);
plot(t, Y, '*', t2, P);
grid on;


% Застосувати стандартні функції MATLAB для інтерполяції функції поліномами.
P_interp = interp1(t, Y, t2, 'linear');
hold on;
plot(t2, P_interp, 'b-');
hold off;

